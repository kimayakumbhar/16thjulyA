package polymorphism;

public class sample1 {
   //method overloading 
	//addition method with 2 integer parameter 
	
	
	public void addition(int a , int b)// non-static method declaration
	{
		System.out.println(a+b);       //method defination
	}
	
	
	//addition method with 3 integer parameter
	public void addition(int a, int b , int c)   //method declaration
	{ //method declaration
		System.out.println(a+b+c);               //method defination
	}
	
	
}
